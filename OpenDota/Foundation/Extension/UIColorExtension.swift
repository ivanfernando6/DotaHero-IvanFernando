import UIKit

extension UIColor {
    class var desertStorm: UIColor? {
        return UIColor(hexString: "#f4f4f2")
    }
    
    class var mercury: UIColor? {
        return UIColor(hexString: "#e8e8e8")
    }
    
    class var frenchGray: UIColor? {
        return UIColor(hexString: "#bbbfca")
    }
    
    class var riverBed: UIColor? {
        return UIColor(hexString: "#495464")
    }
}
